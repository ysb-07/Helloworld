package org;

public class SomeOperations {
	public static void main(String[] args) {
		int x=10;
		int y=20;
		long m=1234567l;
		
		int z=x+y;
		System.out.println(z);
		System.out.println(x+y);
		int n=x+(int)m;
		System.out.println(n);
		
		float f1=12.44f;
		double d1=34.56;
		float ff=f1+(float)d1;
		System.out.println(ff);
		
	}

}
