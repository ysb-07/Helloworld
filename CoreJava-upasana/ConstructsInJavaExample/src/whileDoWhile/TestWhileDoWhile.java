package whileDoWhile;

public class TestWhileDoWhile {
	public static void main(String[] args) {
		int x=11;
		System.out.println(" do while");
		do {
			System.out.println(x);
			x++;
		}while(x<=10);
		
		int y=11;
		System.out.println("while");
		
		while(y<=10)
		{
			System.out.println(y);
			y++;
		}
	}

}
